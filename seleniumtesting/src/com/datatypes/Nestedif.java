package com.datatypes;

public class Nestedif {

	public static void main(String[] args) {

		int num=100;
		if(num<=1)
		{
			System.out.println(num);
		}
		else if(num<20)
			
		{
			System.out.println("fail");
		}
		else if(num>=200)
		{
			System.out.println("pass");
			
		}
		else
		{
			System.out.println("exit");
		}
	}

}
